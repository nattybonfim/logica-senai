package relacionais;

public class DuploIgual {
    public static void main(String[] args) {
    	// declaracao e inicializacao das variaveis
	int x = 4;
	int y = 4;
	
	   // imprimir resultado da expressao x == y
	System.out.println(x == y);
	
	// mudar o valor do x
	x = 3;
	
	// imprimir o resultado da expressao x == y
	System.out.println(x == y);
}
}
