package exemplos;

public class Casting {
	public static void main(String[] args) {

		double num1 = 1000;
		int num2 = 100;

		long resultadoDiv = (long) (num1 / num2);

		System.out.println(resultadoDiv);

		float num3 = 8.5f;
		double num4 = 16.5f;

		long resultadoMult = (long) (num3 * num4);
		System.out.println(resultadoMult);

		// o long

	}

}
